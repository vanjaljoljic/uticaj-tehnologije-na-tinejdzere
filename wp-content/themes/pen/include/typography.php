if ( ! function_exists( 'pen_fonts_all' ) ) {
	/**
	 * List of supported fonts.
	 *
	 * @since Pen 1.0.0
	 */
	function pen_fonts_all() {
		$fonts  = array();
		$google = pen_fonts_google();
		foreach ( $google as $font_name => $font_data ) {
			$fonts[ 'g:' . $font_name ] = sprintf(
				/* Translators: Just some words. */
				__( '%1$s (%2$s)', 'pen' ),
				esc_html( $font_name ),
				esc_html( $font_data['type'] )
			);
		}
		return $fonts;
	}
}

if ( ! function_exists( 'pen_load_fonts_google' ) ) {
	/**
	 * Adds the Google Fonts JavaScript.
	 *
	 * @since Pen 1.0.0
	 */
	function pen_load_fonts_google() {
		$options = array(
			'font_family_site',
			'font_family_headings',
			'font_family_sitetitle',
			'font_family_sitedescription',
			'font_family_phone_header',
			'font_family_phone_footer',
			'font_family_navigation',
			'font_family_navigation_submenu',
			'font_family_forms',
			'font_family_buttons',
			'font_family_title_list',
			'font_family_title_content',
			'font_family_widget_title_top',
			'font_family_widget_title_left',
			'font_family_widget_title_right',
			'font_family_widget_title_bottom',
			'font_family_copyright',
		);

		$google_fonts = array();
		foreach ( $options as $option ) {
			$font = pen_option_get( $option );
			if ( false !== strpos( $font, 'g:' ) ) {
				$google_fonts[] = str_replace( 'g:', '', $font );
			}
		}
		$google_fonts = array_filter( array_unique( $google_fonts ) );
		if ( $google_fonts ) {
			wp_enqueue_script( 'pen-webfont', 'https://ajax.googleapis.com/ajax/libs/webfont/1/webfont.js', array(), PEN_THEME_VERSION, true );
			wp_enqueue_script( 'pen-googlefonts', PEN_THEME_DIRECTORY_URI . '/assets/js/pen-google-fonts.js', array( 'jquery', 'pen-webfont', 'pen-js' ), PEN_THEME_VERSION, true );
			wp_localize_script(
				'pen-googlefonts',
				'pen_googlefonts',
				array(
					'families' => array_values( $google_fonts ),
				)
			);
		}
	}
	add_action( 'wp_enqueue_scripts', 'pen_load_fonts_google' );
}

if ( ! function_exists( 'pen_autoptimize_js_exclude' ) ) {
	/**
	 * Excluding pen-google-fonts.js from Autoptimize compression.
	 *
	 * @param string $exclude comma-separated list of exclude strings.
	 *
	 * @return: comma-separated list of exclude strings
	 */
	function pen_autoptimize_js_exclude( $exclude ) {
		return $exclude . ',pen-google-fonts.js';
	}
	add_filter( 'autoptimize_filter_js_exclude', 'pen_autoptimize_js_exclude', 10, 1 );
}
